-module(test_cowboy_sup).
-behaviour(supervisor).

%% Callbacks
-export([
	start_link/0,
	init/1
]).

%%% ==================================================================
%%% Callback functions
%%% ==================================================================

%% -------------------------------------------------------------------
%% @private
%% -------------------------------------------------------------------
-spec start_link() -> {ok, pid()}.

start_link() ->
	supervisor:start_link({local, ?MODULE}, ?MODULE, []).

%% -------------------------------------------------------------------
%% @private
%% -------------------------------------------------------------------
-spec init(term()) -> {'ok', tuple()}.

init([]) ->
	Procs = [],
	{ok, {{one_for_one, 10, 10}, Procs}}.
